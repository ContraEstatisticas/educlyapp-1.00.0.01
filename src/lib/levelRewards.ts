const interpolate = (template: string, values: Record<string, string | number>) => {
  return Object.entries(values).reduce((result, [key, value]) => {
    return result.replace(new RegExp(`{{${key}}}`, "g"), String(value));
  }, template);
};

export const normalizeRewardLocale = (language?: string): RewardLocale => {
  const normalized = (language || "en").toLowerCase().split("-")[0] as RewardLocale;
  return COPY_BY_LOCALE[normalized] ? normalized : "en";
};

export const getLevelRewardsCopy = (language?: string) => COPY_BY_LOCALE[normalizeRewardLocale(language)];
export const getRewardLevel = (rewardKey: LevelRewardKey) => REWARD_LEVELS[rewardKey];
export const getRewardDetails = (rewardKey: LevelRewardKey, language?: string) => getLevelRewardsCopy(language).rewardMap[rewardKey];
export const getRewardTitleList = (rewardKeys: LevelRewardKey[], language?: string) => rewardKeys.map((rewardKey) => getRewardDetails(rewardKey, language).title);
export const getUnlockedRewardForMilestone = (rewards: LevelRewardRow[], milestoneLevel: number) => rewards.find((reward) => reward.source_level === milestoneLevel) || null;
export const getLevelTitle = (level: number, language?: string) => `${getLevelRewardsCopy(language).levelLabel} ${level}`;
export const getLevelUpToastTitle = (level: number, language?: string) => interpolate(getLevelRewardsCopy(language).levelUpTitleTemplate, { level });
export const getLevelUpToastDescription = (level: number, language?: string) => interpolate(getLevelRewardsCopy(language).levelUpDescriptionTemplate, { level, levelLabel: getLevelRewardsCopy(language).levelLabel });
export const formatRewardExpiry = (value: unknown, language?: string) => {
  if (typeof value !== "string") return null;
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return null;
  const locale = normalizeRewardLocale(language);
  const intlLocale = { pt: "pt-BR", en: "en-US", es: "es-ES", fr: "fr-FR", de: "de-DE", it: "it-IT", ru: "ru-RU", zh: "zh-CN", ja: "ja-JP", ko: "ko-KR", ar: "ar-SA", hi: "hi-IN", tr: "tr-TR", pl: "pl-PL", nl: "nl-NL" }[locale];
  return new Intl.DateTimeFormat(intlLocale, { dateStyle: "short", timeStyle: "short" }).format(date);
};
